/*
 * build-table-sample.js — regenerate samples/table-merge-shade.doc.
 *
 * A small clean-room demo of table cell *merging* and *shading*, written with
 * the bundled writer (src/textToDoc.js). Horizontal merges use Word's native
 * representation: a row with fewer, wider cells (the merged cell carries a
 * column span via its rgdxaCenter boundaries) — the same shape Word's "Merge
 * Cells" command produces. Shading is a per-cell background COLORREF.
 *
 *   node scripts/build-table-sample.js
 */
var fs = require('fs');
var path = require('path');
var textToDoc = require('../src/textToDoc.js');

// COLORREF is 0x00BBGGRR (red is the low byte).
var GREY = 0xC0C0C0, GREEN = 0x00FF00, RED = 0x0000FF, YELLOW = 0x00FFFF;
var FULL = [0, 3000, 6000, 9000];   // three equal columns
function row3(a, b, c) { return [{ runs: [{ text: a.t || a }], kind: 'cell', shd: a.shd }, { runs: [{ text: b.t || b }], kind: 'cell', shd: b.shd }, { runs: [{ text: c.t || c }], kind: 'rowEnd', shd: c.shd, tblw: FULL }]; }

var model = {
  props: { title: 'Cell merging & shading', author: 'Alpaq92', subject: 'JSDoc table fidelity demo' },
  body: [].concat(
    [{ runs: [{ text: 'Cell merging & shading', b: true, size: 15 }], kind: 'p' }],
    [{ runs: [{ text: 'A table whose header is merged across all three columns, with shaded status cells and a part-width merge on the last row.' }], kind: 'p' }],
    // Row 1 — header merged across all three columns (one wide cell), grey fill.
    [{ runs: [{ text: 'Project status — Q2', b: true }], kind: 'rowEnd', shd: GREY, tblw: [0, 9000] }],
    // Row 2 — column headings.
    row3({ t: 'Phase' }, { t: 'State' }, { t: 'Owner' }),
    // Rows 3–5 — a shaded State cell per row.
    row3('Design', { t: 'Done', shd: GREEN }, 'Alpaq92'),
    row3('Build', { t: 'Blocked', shd: RED }, 'Alpaq92'),
    row3('Ship', { t: 'Pending', shd: YELLOW }, 'Alpaq92'),
    // Row 6 — "Overall" merged across the first two columns.
    [{ runs: [{ text: 'Overall', b: true }], kind: 'cell' }, { runs: [{ text: 'On track', b: true }], kind: 'rowEnd', tblw: [0, 6000, 9000] }],
    [{ runs: [{ text: 'Generated by scripts/build-table-sample.js (the bundled writer).' }], kind: 'p' }]
  )
};

var out = path.join(__dirname, '..', 'samples', 'table-merge-shade.doc');
fs.writeFileSync(out, Buffer.from(textToDoc(model)));
console.log('wrote ' + out + ' (' + fs.statSync(out).size + ' bytes)');
